export function getAppThemeTokens(isDark: boolean): AppThemeTokens {
  return {
    mode: isDark ? 'dark' : 'light',
    cssVars: isDark ? darkVars : lightVars,
    antdToken: isDark ? darkAntdToken : lightAntdToken,
  };
}

export function cssVarsToString(cssVars: CssVars): string {
  return Object.entries(cssVars)
    .map(([name, value]) => `        ${name}: ${value};`)
    .join('\n');
}
